public class datatype {
    public static void main(String[] args) {

        //Integer types
        int myNum = 100000; // Integer (whole number)
        long myLongNum = 15000000000L; // Long (larger whole number)
        short myShortNum = 5000; // Short (smaller whole number)
        byte myByteNum = 100; // Byte (very small whole number)

        //Decimal types
        float myFloatNum = 5.75f; // Float (decimal number with single precision) 7 decimal places.
        double myDoubleNum = 19.99; // Double (decimal number with double precision) 15 decimal places.

        // Other types
        char myChar = 'A'; // Character (single character)
        boolean myBool = true; // Boolean (true or false)

        // Output the values
        System.out.println("Integer: " + myNum);
        System.out.println("Long: " + myLongNum);
        System.out.println("Short: " + myShortNum); 
        System.out.println("Byte: " + myByteNum);
        System.out.println("Float: " + myFloatNum);
        System.out.println("Double: " + myDoubleNum);
        System.out.println("Character: " + myChar);
        System.out.println("Boolean: " + myBool);
        
    }
}
